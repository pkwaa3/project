package vo;

public class Company {
	private String com_id;
	private String com_pw;
	private String com_admin;
	
	public String getCom_id() {
		return com_id;
	}
	public void setCom_id(String com_id) {
		this.com_id = com_id;
	}
	public String getCom_pw() {
		return com_pw;
	}
	public void setCom_pw(String com_pw) {
		this.com_pw = com_pw;
	}
	public String getCom_admin() {
		return com_admin;
	}
	public void setCom_admin(String com_admin) {
		this.com_admin = com_admin;
	}
	
	
}
